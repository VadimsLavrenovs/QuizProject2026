    private void RegPogaRegMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RegPogaRegMouseClicked
        try {
            String name = jTextField4.getText().trim();
            String surname = jTextField5.getText().trim();
            String login = jTextField6.getText().trim();
            String pass1 = jTextField7.getText();
            String pass2 = jTextField9.getText();

            if (name.isEmpty() || surname.isEmpty() || login.isEmpty() || pass1.isEmpty() || pass2.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Aizpildi visus laukus!");
                return;
            }

            if (!pass1.equals(pass2)) {
                JOptionPane.showMessageDialog(null, "Paroles nesakrīt!");
                return;
            }

            boolean ok = User.registerStudent(name, surname, login, pass1);

            if (!ok) {
                JOptionPane.showMessageDialog(null, "Šāds lietotājvārds jau eksistē!");
                return;
            }

            JOptionPane.showMessageDialog(null, "Reģistrācija veiksmīga!");
            dispose();
            this.setVisible(true);
            RegistracijasLogs.setVisible(false);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Nezināma kļūda programmā");
            Logger.getLogger(MainUserInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_RegPogaRegMouseClicked

